
# Criar um programa onde o usuário digite dois valores e apareça a soma

print('iniciando o programa')
numero_1=float(input('Digite o primeiro numero: '))
numero_2=float(input('Digite o primeiro numero: '))
soma_numero=numero_1+numero_2

print(f'A soma do primeiro numero {numero_1} com o segundo numero {numero_2} é de {soma_numero:.2f}!')
exit